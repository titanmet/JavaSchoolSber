package com.ratnikov.HW.HW1;

public class Field {
    private int xField;
    private int yField;

    public Field(int xF, int yF) {
        this.xField = xF;
        this.yField = yF;
    }

    public int getxField() {
        return xField;
    }

    public int getyField() {
        return yField;
    }
}
